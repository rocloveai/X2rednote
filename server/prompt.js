const STYLE_PROMPTS = {
  news: `\n\n## 当前风格：资讯分析型\n\n- 以事件为核心，快速传递关键信息\n- 开头一句话说清楚发生了什么\n- 中间用编号列表拆解要点\n- 结尾给出简短评价或意义判断\n- 语气：简洁、专业、信息密度高`,

  opinion: `\n\n## 当前风格：深度观点型\n\n- 以观点为核心，带有分析和判断\n- 开头点出核心论点\n- 中间用逻辑链条展开论证\n- 结尾给出明确的立场和展望\n- 语气：理性、有深度、有自己的判断`
}

export function buildMessages(text, style, background) {
  const stylePrompt = STYLE_PROMPTS[style] || STYLE_PROMPTS.news

  let userContent = '请将以下X平台内容改写为专业风格的图文素材：\n\n## 用户提供的原始内容\n---\n' + text + '\n---'

  if (background) {
    userContent += '\n\n## 搜索引擎补充的背景信息（仅作参考，用于补充事实和细节）\n---\n' + background + '\n---'
    userContent += '\n\n注意：以用户提供的内容为主线，搜索到的背景信息仅用于补充事实细节、增加信息量。不要偏离用户内容的核心主题。'
  }

  userContent += '\n\n请严格按JSON格式输出。'

  return [
    {
      role: 'system',
      content: SYSTEM_PROMPT + stylePrompt
    },
    {
      role: 'user',
      content: userContent
    }
  ]
}
